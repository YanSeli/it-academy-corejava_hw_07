package com.company.task124A4;

public class Region {
    private String regionName;
    private double square;

    public Region(String regionName, double square) {
        this.regionName = regionName;
        this.square = square;
    }

    public String getRegionName() {
        return regionName;
    }

    public double getSquare() {
        return square;
    }
}
